package lynda.essentials.numbers;

public class OperatorsTest {
//implicit casting
    public static void main(String[] args) {
	System.out.println(1 / 10);
	System.out.println(15 / 10);
	System.out.println(15 / 10.0);
	System.out.println(15.0 / 10.0);
	System.out.println(20.0 / 10);
	System.out.println(22.0 % 10);
	System.out.println(22 % 10);
    }
}
